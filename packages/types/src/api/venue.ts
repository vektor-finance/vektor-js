/**
 * On-chain venue
 * @export
 * @interface Venue
 */
export interface Venue {
  /**
   * Venue id
   * @type {string}
   * @memberof Venue
   */
  id: string
  /**
   * Venue name
   * @type {string}
   * @memberof Venue
   */
  name: string
  /**
   * Venue type
   * @type {string}
   * @memberof Venue
   */
  type: string
  /**
   * Venue url
   * @type {string}
   * @memberof Venue
   */
  url: string
}
